from math import floor


def main(y, x, z):
    result = 0
    n = len(x)
    for i in range(0, n):
        result += 24 * (70 * z[(n - 1) + 1 - (floor(i) + 1)] +
                        ((x[(n - 1) + 1 - (floor(i) + 1)])**3 / 80) +
                        66 * y[i] ** 2) ** 7
    return 78 * result


